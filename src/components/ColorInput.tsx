function ColorInput({ label, value, onChange } : ColorInputProps ) {
  const setValueChanged = debounce(onChange, 300);
  const refColorInput = useRef<HTMLInputElement>(null);
  const blobsTo = useMemo(() => [...shuffle(PossibleBlob), blobStart], []);

  const springsIcon = useSpring( 
    {
      from : blobStart,
      to: blobsTo,
      loop: true,
      config: { duration: 1000 }

    }
  );

  function openColorInput() {
    if(!refColorInput.current) {
      return;
    }

    refColorInput.current.click();
  }

  return (
    <div className="flex flex-col height">
      <label className="flex flex-row items-center gap-2">
        {label}
      </label>
      <div className="flex flex-row gap-2">
        <button className="btn btn-dash" onClick={openColorInput}>{value}</button>
      <div className="relative" style={{ bottom: 60, height: 50 }}>
        <input
          ref={refColorInput}
          id="colorInput"
          className="hover:cursor-pointer relative"
          style={{ top: 60, left: 10, zIndex: -1 }}
          type="color"
          value={value}
          onChange={(e) => setValueChanged(e.target.value)}
        />
        <div onClick={openColorInput} style={{ zIndex: 1 }}>
          <svg width="64" height="64" viewBox="0 0 200 200" className="hover:cursor-pointer">
            <animated.path 
              fill={value} 
              stroke="#000000"
              strokeWidth={1.5795}
              strokeMiterlimit={10}
              transform="translate(100 100)"
              d={springsIcon.d}
            >
            </animated.path>
          </svg>
        </div>
      </div>
    </div>

    </div>
  );
}

